// Function to convert geometry to MULTIPOLYGON string
function convertToMultipolygon(geometry) {
  const rings = geometry.rings.map(ring => {
    // Convert each ring (array of coordinates) to the required format
    return `(${ring.map(([lon, lat]) => `${lat} ${lon}`).join(', ')})`;
  });

  // Wrap all rings in MULTIPOLYGON and return
  return `MULTIPOLYGON(${rings.join(', ')})`;
}

